S = 'Hello world!'

print(f'\n {S}') # Printing a string.
print(f'\n {type(S)}') # Type of a string.
print(f'\n {len(S)}') # Size of a string.

print('\n My world ' + 'blue') # Concatenation.

 # Replaces a substring with some other string.
S1 = S.replace('world', 'people')
print(f'\n {S1}')